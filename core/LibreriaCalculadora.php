<?php
    /**
     * Creo una función que hace una suma de dos números que le paso como parámetro.
     * @param int | float $numero1
     * @param int | float $numero2
     * @return int | float Devuelve la suma de los dos números pasados como parámetro.
     */
    function suma ($numero1, $numero2){
        return $numero1+$numero2; 
    }
    /**
     * Creo una función que hace una resta de dos números que le paso como parámetro.
     * @param int | float $numero1
     * @param int | float $numero2
     * @return int | float Devuelve la resta de los dos números pasados como parámetro.
     */
    function resta ($numero1, $numero2){
        return $numero1-$numero2;
    }
    /**
     * Creo una función que hace una multiplicación de dos números que le paso como parámetro.
     * @param int | float $numero1
     * @param int | float $numero2
     * @return int | float Devuelve la multiplicación de los dos números pasados como parámetro.
     */
    function multiplicacion ($numero1, $numero2){
        return $numero1*$numero2;
    }
    /**
     * Creo una función que hace una división de dos números que le paso como parámetro.
     * @param int | float $numero1
     * @param int | float $numero2
     * @return int | float Devuelve la división de los dos números pasados como parámetro.
     */
    function division ($numero1, $numero2){
        return $numero1/$numero2;
    }

